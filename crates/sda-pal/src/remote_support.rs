//! Remote-support session capture & transport.
//!
//! This module is the cross-platform PAL surface for the
//! Device Control remote-support feature.  The
//! [`RemoteSupportProvider`] trait is the binding spec from
//! `docs/architecture.md` § 4.1 (Trait surface); per-OS implementations
//! ship in this crate behind `cfg`-gates.
//!
//! Current scope (this file): every per-OS implementation is a
//! deliberate **stub** that returns
//! [`RemoteSupportError::NotSupported`] from
//! [`RemoteSupportProvider::start_session`]. The real capture +
//! transport stacks land in later phases:
//!
//! * **Linux** — PipeWire / XCB capture (Wayland + X11), gated on
//!   explicit user consent — see `docs/device-control.md` § 9.
//! * **macOS** — ScreenCaptureKit-style API.
//! * **Windows** — Windows Graphics Capture (WGC) + DDA-style
//!   transport.
//!
//! Higher-level orchestration (consent flow, time-bounded sessions,
//! evidence emission) lives in `crates/sda-remote-support`. That
//! crate calls into this trait via `Box<dyn RemoteSupportProvider>`
//! so the supervisor can swap a real provider for the stub in
//! tests without touching production code.

use chrono::{DateTime, Duration, Utc};
use serde::{Deserialize, Serialize};
use std::io;

/// Errors produced by [`RemoteSupportProvider`] implementations.
#[derive(Debug, thiserror::Error)]
pub enum RemoteSupportError {
    /// The underlying capture API or transport returned an error.
    #[error("remote-support backend failed: {0}")]
    Backend(String),
    /// The host platform has no capture backend yet.
    #[error("remote-support not supported on this platform")]
    NotSupported,
    /// The user declined the consent prompt.
    #[error("remote-support consent denied")]
    ConsentDenied,
    /// Caller-supplied input failed validation.
    #[error("remote-support input invalid: {0}")]
    InvalidInput(String),
    /// I/O error while talking to the OS capture / transport stack.
    #[error("remote-support IO error: {0}")]
    Io(#[from] io::Error),
}

/// Inputs to [`RemoteSupportProvider::start_session`].
///
/// `operator_id` identifies the human or service-account requesting
/// the session — it surfaces in the consent UI so the end-user can
/// see exactly who is asking. `max_duration` is a hard wall-clock
/// cap enforced by `sda-remote-support`; the PAL layer is allowed
/// to truncate sessions earlier if the underlying capture API has
/// its own limit. `consent_required` MUST be `true` for any
/// production session; the field exists so unit tests can build
/// `SessionParams` without a consent fixture.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct SessionParams {
    /// Opaque, server-issued identifier for the operator requesting
    /// the session (e.g. helpdesk agent username, automation tag).
    pub operator_id: String,
    /// Hard wall-clock cap on the session length. The supervisor
    /// also enforces this; the PAL layer simply gets it for log
    /// context.
    pub max_duration: Duration,
    /// Whether the agent must show a consent banner and block until
    /// the user accepts. Always `true` outside unit tests per
    /// `docs/device-control.md` § 9.
    pub consent_required: bool,
}

/// Opaque handle to a started remote-support session.
///
/// `session_id` is generated by the agent (UUIDv4) so the control
/// plane and the local audit chain can correlate the session
/// without trusting an operator-supplied identifier.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub struct SessionHandle {
    /// Agent-issued, opaque session ID.
    pub session_id: String,
    /// Wall-clock UTC start time of the session.
    pub started_at: DateTime<Utc>,
}

/// Cross-platform remote-support capture & transport surface.
///
/// Implementations MUST be `Send + Sync` because the supervisor
/// holds a `Box<dyn RemoteSupportProvider>` across `await` points
/// in the Tokio runtime.  Per-OS stubs are zero-sized and trivially
/// satisfy these bounds.
pub trait RemoteSupportProvider: Send + Sync {
    /// Begin a remote-support session subject to `params`.
    ///
    /// Per-OS stubs return [`RemoteSupportError::NotSupported`].
    fn start_session(&self, params: &SessionParams) -> Result<SessionHandle, RemoteSupportError>;

    /// Tear down a running session. Idempotent — calling
    /// `end_session` on an already-terminated handle MUST NOT
    /// return an error.
    fn end_session(&self, handle: &SessionHandle) -> Result<(), RemoteSupportError>;
}

// =====================================================================
// Per-OS stub providers
// =====================================================================

/// Linux stub provider.  Will host the PipeWire / XCB
/// capture backend in a later phase; today every call returns
/// [`RemoteSupportError::NotSupported`].
#[cfg(target_os = "linux")]
pub mod linux_impl {
    use super::*;

    /// Linux stub for [`RemoteSupportProvider`].
    #[derive(Debug, Default)]
    pub struct LinuxRemoteSupportProvider;

    impl LinuxRemoteSupportProvider {
        /// Construct a fresh stub provider.
        pub fn new() -> Self {
            Self
        }
    }

    impl RemoteSupportProvider for LinuxRemoteSupportProvider {
        fn start_session(
            &self,
            _params: &SessionParams,
        ) -> Result<SessionHandle, RemoteSupportError> {
            Err(RemoteSupportError::NotSupported)
        }

        fn end_session(&self, _handle: &SessionHandle) -> Result<(), RemoteSupportError> {
            // Idempotent — even on the stub end_session must succeed
            // so the supervisor's cleanup paths stay simple.
            Ok(())
        }
    }
}

#[cfg(target_os = "linux")]
pub use linux_impl::LinuxRemoteSupportProvider;

