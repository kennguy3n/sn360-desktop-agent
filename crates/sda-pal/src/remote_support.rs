// =====================================================================
// Default factory
// =====================================================================

/// Returns the platform-default [`RemoteSupportProvider`] for this
/// host.
///
/// On unsupported targets returns `None` so the agent can run with
/// remote-support disabled rather than panicking at startup.
pub fn default_remote_support_provider() -> Option<Box<dyn RemoteSupportProvider>> {
    #[cfg(target_os = "linux")]
    {
        Some(Box::new(LinuxRemoteSupportProvider::new()))
    }
    #[cfg(target_os = "macos")]
    {
        Some(Box::new(MacRemoteSupportProvider::new()))
    }
    #[cfg(target_os = "windows")]
    {
        Some(Box::new(WindowsRemoteSupportProvider::new()))
    }
    #[cfg(not(any(target_os = "linux", target_os = "macos", target_os = "windows")))]
    {
        None
    }
}

// =====================================================================
// Tests
// =====================================================================

#[cfg(test)]
mod tests {
    use super::*;

    fn sample_params() -> SessionParams {
        SessionParams {
            operator_id: "ops@example.com".into(),
            max_duration: Duration::minutes(30),
            consent_required: true,
        }
    }

    fn sample_handle() -> SessionHandle {
        SessionHandle {
            session_id: "sess-deadbeef".into(),
            started_at: Utc::now(),
        }
    }

    #[test]
    fn session_params_round_trips_through_json() {
        let p = sample_params();
        let json = serde_json::to_string(&p).expect("encode");
        let back: SessionParams = serde_json::from_str(&json).expect("decode");
        assert_eq!(p, back);
    }

    #[test]
    fn session_handle_round_trips_through_json() {
        let h = sample_handle();
        let json = serde_json::to_string(&h).expect("encode");
        let back: SessionHandle = serde_json::from_str(&json).expect("decode");
        assert_eq!(h, back);
    }

    #[test]
    fn default_provider_is_present_on_supported_targets() {
        // The factory MUST return Some on every target the workspace
        // supports — Linux/macOS/Windows. If a future port lands on a
        // target with no backend, this assertion will fail and remind
        // us to extend the factory or return None deliberately.
        #[cfg(any(target_os = "linux", target_os = "macos", target_os = "windows"))]
        {
            let p = default_remote_support_provider();
            assert!(p.is_some(), "expected platform stub provider");
        }
    }

    #[test]
    fn stub_start_session_returns_not_supported() {
        // The per-OS stubs deliberately fail closed. End-to-end
        // tests in `sda-remote-support` rely on this so the
        // supervisor exposes the NotSupported state on the bus.
        let provider = default_remote_support_provider();
        if let Some(provider) = provider {
            let err = provider.start_session(&sample_params()).err();
            assert!(matches!(err, Some(RemoteSupportError::NotSupported)));
        }
    }

    #[test]
    fn stub_end_session_is_idempotent() {
        // end_session must succeed on an unknown handle so the
        // supervisor's revoke / shutdown paths can be unconditional.
        let provider = default_remote_support_provider();
        if let Some(provider) = provider {
            let h = sample_handle();
            assert!(provider.end_session(&h).is_ok());
            assert!(
                provider.end_session(&h).is_ok(),
                "second call must also succeed"
            );
        }
    }

    #[test]
    fn trait_object_compiles_for_send_sync_and_box() {
        // Compile-time check: the trait must be object-safe AND
        // `Send + Sync` so the supervisor can hold it across
        // `await` points.
        fn _assert_object_safe(_: Box<dyn RemoteSupportProvider>) {}
        fn _assert_send_sync<T: Send + Sync>() {}
        _assert_send_sync::<Box<dyn RemoteSupportProvider>>();
    }
}
